<template>
	<button @click="goBack">back</button>
</template>

<script lang="ts">
	// ----------------------------------------------------------------
	// ----------------------------------------------------------------
	// ----------------------------------------------------------------

	import { Component, Vue, } from 'vue-property-decorator';

	// ----------------------------------------------------------------
	// ----------------------------------------------------------------
	// ----------------------------------------------------------------

	@Component({
		components: {
		},
	})
	export default class ComponentBack extends Vue{
		private goBack(): void{
			(window.history.length > 1) ? this.$router.go(-1) : this.$router.push('/');
		}
	}

	// ----------------------------------------------------------------
	// ----------------------------------------------------------------
	// ----------------------------------------------------------------
</script>

